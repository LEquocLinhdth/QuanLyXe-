CREATE PROCEDURE [dbo].[GetImportedProducts]
    @Month INT,
    @Year INT
AS
BEGIN
    SELECT
        HANGHOA.MAHH,
        HANGHOA.TENHH,
        SUM(CTPN.SLUONG) AS SoLuongNhap
    FROM
        CTPN
    INNER JOIN
        PHIEUNHAP ON CTPN.MAPN = PHIEUNHAP.MAPN
    INNER JOIN
        HANGHOA ON CTPN.MAHH = HANGHOA.MAHH
    WHERE
        MONTH(PHIEUNHAP.NgayLap) = @Month
        AND YEAR(PHIEUNHAP.NgayLap) = @Year
    GROUP BY
        HANGHOA.MAHH, HANGHOA.TENHH;
END;
